package dataStructureAndAlgorithm._1_array;

public class MinFunction {

	public static void main(String[] args) {
		double a = 12.123;
		double b = 12.456;
		double x = 23;
		double y = -23;
		double m = -25;
		double n = -23;
		
		System.out.println(Math.min(a, b));
		System.out.println(Math.min(x, y));
		System.out.println(Math.min(m, n));

	}
}

/*
12.123
-23.0
-25.0
*/
