package javastrings;

public class CharAt {

	public static void main(String[] args) {
		String s1 = "Mainul";
		System.out.println(s1.charAt(0));
		System.out.println(s1.charAt(3));
		for (int i = 0; i < s1.length(); i++) {
			System.out.println(s1.charAt(i));
		}
		
		System.out.println(s1.length());

	}

}
